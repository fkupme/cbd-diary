// Промпты потока intake («разложить по полочкам»).
// Gemini вызывается в JSON-режиме (responseJson: true) — описываем только схему,
// парсинг и валидация — на стороне IntakeService.

/**
 * Сегментация монолога на отдельные КПТ-ситуации.
 * Транскрипт подаётся отдельным user-сообщением; модель возвращает
 * { "situations": [{ "title": string, "quote": string }] }.
 */
export function buildSegmentationPrompt(): string {
  return [
    'Ты — ассистент КПТ-дневника. На вход дан монолог пользователя (расшифровка',
    'голосовой заметки), где он описывает, что с ним произошло. Раздели его на',
    'ОТДЕЛЬНЫЕ ситуации — каждая это один момент/триггер, на который человек',
    'как-то отреагировал (мелькнула мысль, возникло чувство, что-то сделал).',
    '',
    'Правила:',
    '- Каждая ситуация — самостоятельное событие, про которое можно отдельно',
    '  спросить «что ты подумал, что почувствовал в этот момент».',
    '- title: короткая формулировка от первого лица на языке пользователя,',
    '  до 7 слов, без точки в конце.',
    '- quote: дословный фрагмент из монолога, к которому относится ситуация.',
    '- НЕ придумывай того, чего нет в тексте. Если событие одно — верни одну ситуацию.',
    '- Порядок — как в рассказе. Максимум 8 ситуаций.',
    '',
    'Пример. Монолог: «пришёл к Васе, не смог попасть в подъезд, стоял ждал,',
    'потом он спустился и говорит чё так долго».',
    'Ответ: {"situations":[',
    '  {"title":"Не смог попасть в подъезд","quote":"не смог попасть в подъезд"},',
    '  {"title":"Ждал, пока Вася спустится","quote":"стоял ждал"},',
    '  {"title":"Вася спросил «чё так долго»","quote":"он спустился и говорит чё так долго"}',
    ']}',
    '',
    'Верни СТРОГО JSON вида {"situations":[{"title":string,"quote":string}]} и ничего больше.',
  ].join('\n');
}

/** Русская плюрализация слова «ситуация» по числу. */
export function pluralSituations(n: number): string {
  const mod10 = n % 10;
  const mod100 = n % 100;
  if (mod10 === 1 && mod100 !== 11) return 'ситуацию';
  if (mod10 >= 2 && mod10 <= 4 && (mod100 < 10 || mod100 >= 20)) return 'ситуации';
  return 'ситуаций';
}

/**
 * Маппинг свободного описания чувств в id эмоций из каталога.
 * Текст пользователя подаётся отдельным user-сообщением; модель возвращает
 * { "emotionIds": number[] } — 1..4 наиболее подходящих id (или пустой массив).
 */
export function buildEmotionMappingPrompt(catalog: string): string {
  return [
    'Пользователь описал свои чувства своими словами. Сопоставь их с эмоциями',
    'из каталога ниже. Правила:',
    '- бери только то, что человек действительно выразил; от 1 до 4 эмоций;',
    '- если ничего не подходит — пустой массив;',
    '- НЕ придумывай эмоций вне каталога, возвращай только id из него;',
    '- intensity (сила) 1..10: если пользователь назвал число рядом с эмоцией',
    '  («тревога 8», «злюсь на 3») — возьми его; иначе поставь 5.',
    '',
    'Каталог (id: название (синонимы)):',
    catalog,
    '',
    'Верни СТРОГО JSON {"emotions":[{"id":number,"intensity":number}]} и ничего больше.',
  ].join('\n');
}
