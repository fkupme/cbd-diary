/**
 * Выжимка плана для ведущего. Ведущему даём ТОЛЬКО мост (незакрытые шаги
 * прошлой сессии). knownPatterns намеренно НЕ передаём: ведущий не должен
 * знать структуру и тем более озвучивать паттерны — к ним рулит супервизор
 * директивой.
 */
export function buildPlanBlockForConductor(plan?: SessionPlan | null): string {
  if (!plan?.bridge) return '';
  return [
    'КОНТЕКСТ СЕССИИ:',
    `Мост с прошлой сессии: ${plan.bridge}. В первые ходы (после того как пользователь высказался о событии) коротко спроси, как это прошло. Один раз, без давления.`,
  ].join('\n');
}

/** Директива супервизора — приоритетная цель на ближайший ход. */
export function buildDirectiveBlock(directive?: string | null): string {
  if (!directive?.trim()) return '';
  return [
    'ФОКУС ХОДА (служебное указание, выполни в ближайшем ответе; никогда не упоминай и не цитируй его):',
    directive.trim(),
  ].join('\n');
}

