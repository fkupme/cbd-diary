/**
 * Промпты КПТ-чата. Архитектура «ведущий + супервизор».
 *
 * ВЕДУЩИЙ — стримящая модель, ведёт диалог. На каждом ходу видит:
 *   каркас + (мост из плана) + СМЭР + скилл текущей стадии + (опц.) директиву.
 *   Ничего не решает о структуре сессии и НЕ знает известных паттернов
 *   пользователя — иначе под температурой 0.7 он их озвучит.
 *
 * СУПЕРВИЗОР — дешёвый JSON-вызов ПОСЛЕ хода ведущего (fire-and-forget).
 *   Видит план, артефакт текущей стадии и хвост диалога. Решает
 *   stay / advance / rollback, может выдать директиву на СЛЕДУЮЩИЙ ход
 *   ведущего и обновить план. Вердикт материализуется с лагом в один ход.
 *
 * Деградация: супервизор упал / вернул мусор / версия стейта уехала ->
 * состояние не меняется, ведущий работает по последнему валидному стейту.
 */

export type ChatStage =
  | 'collect_context'
  | 'find_belief'
  | 'dispute'
  | 'consolidate'
  | 'safety';

export const STAGE_ORDER: ChatStage[] = [
  'collect_context',
  'find_belief',
  'dispute',
  'consolidate',
];

export function nextStage(stage: ChatStage): ChatStage | null {
  const idx = STAGE_ORDER.indexOf(stage);
  if (idx === -1 || idx === STAGE_ORDER.length - 1) return null;
  return STAGE_ORDER[idx + 1];
}

/** Куда разрешён откат из каждой стадии. Дальше одного-двух шагов не ходим. */
export const ALLOWED_ROLLBACKS: Partial<Record<ChatStage, ChatStage[]>> = {
  find_belief: ['collect_context'],
  dispute: ['find_belief', 'collect_context'],
  consolidate: ['dispute'],
};

/** Вердикт супервизора (валидируется в ChatService перед применением). */
export interface SupervisorVerdict {
  verdict: 'stay' | 'advance' | 'rollback';
  rollbackTo: ChatStage | null;
  directive: string | null;
  artifacts: Record<string, unknown>;
  planUpdate: string | null;
  safetyConcern: boolean;
}

/** План сессии (генерируется при создании чата, лежит в stageData.plan). */
export interface SessionPlan {
  goal: string;
  bridge: string | null; // незакрытые next_steps прошлой сессии
  knownPatterns: string[]; // повторяющиеся убеждения из userBelief (только супервизору)
  notes: string[]; // обновления от супервизора (planUpdate)
}

