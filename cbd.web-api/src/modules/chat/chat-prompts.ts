export function buildStageInstruction(stage: ChatStage): string {
  return STAGE_SKILLS[stage] ?? STAGE_SKILLS.collect_context;
}

/**
 * Сборка системного промпта хода ведущего.
 * Порядок: каркас -> контекст сессии -> СМЭР -> скилл стадии -> директива.
 * Директива последней — у свежего указания максимальный вес.
 */
export function buildConductorPrompt(params: {
  user: any;
  entry: any;
  emotionLabels: Record<number, string>;
  stage: ChatStage;
  plan?: SessionPlan | null;
  directive?: string | null;
}): string {
  const { user, entry, emotionLabels, stage, plan, directive } = params;
  return [
    buildScaffold(user),
    buildPlanBlockForConductor(plan),
    buildSmerBlock(entry, emotionLabels),
    buildStageInstruction(stage),
    buildDirectiveBlock(directive),
  ]
    .filter(Boolean)
    .join('\n\n');
}

